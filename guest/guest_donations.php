at need in a third world country like the Philippines. Not only does it lift the population out of the dredges of illiteracy and unproductibility, but it also builds the church. In these four years since we began the church planting work in 1997, the church has grown from scratch to 1300 each Sunday. Presently, the carport church has outgrown its facilities and has moved to the school campus.<br><br>This entire work has been accomplished independently, since most mission groups will not support a biracial couple. Through gifts from family, friends, and churches, this work is flourishing, lives are being mended, and souls are receiving eternal life.</p>
